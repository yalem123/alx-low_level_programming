/**
 * int_index - function that searches for an integer
 * @array: input integer array
 * @size: size of the array
 * @action: pointer to the function
 */
int int_index(int *array, int size, int (*cmp)(int))
{
unsigned int i;
if (array && action)
for (i = 0; i < size; i++)
action(array[i]);
}
